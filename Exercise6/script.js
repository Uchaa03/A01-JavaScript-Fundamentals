let input = prompt("Enter a number");
let num = Number(input);

//Check number and empty entries
if (input === `${num}`){
    // Both requisites -> Fist Option
    // First requisite -> Second Option
    // Second requisite -> Third Option
    num >= 0? num <= 0?
            alert("Number is 0") :
            alert("Number is Positive") :
            alert("Number is Negative");

}else {
    alert("The value is not a number")
}
